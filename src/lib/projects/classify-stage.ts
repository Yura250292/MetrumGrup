function normalize(s: string | null | undefined): string {
  return (s ?? "").trim().toLowerCase();
}

export function classifyStageByName(name: string | null | undefined): StageCostType {
  const lower = normalize(name);
  if (!lower) return "OTHER";

  // Явна мітка матеріалу замовника часто йде в дужках в кінці назви.
  if (
    lower.includes("матеріал замовника") ||
    lower.includes("(матеріал)") ||
    lower.startsWith("матеріал")
  ) {
    return "MATERIAL";
  }

  for (const p of LABOR_PREFIXES) {
    if (lower.startsWith(p)) return "LABOR";
  }

  for (const kw of MATERIAL_KEYWORDS) {
    if (lower.includes(kw)) return "MATERIAL";
  }

  return "OTHER";
}
