export type TaskSpecPromptInput = {
  title: string;
  description?: string;
  projectTitle?: string;
  projectStage?: string;
  assignerName?: string;
  today: string;
};

export function buildTaskSpecUserPrompt(input: TaskSpecPromptInput): string {
  const weekday = new Date(input.today).toLocaleDateString("uk-UA", {
    weekday: "long",
  });
  const titleWordCount = input.title.trim().split(/\s+/).length;
  const descLen = (input.description ?? "").trim().length;
  // Hint модель: якщо мало даних — мінімалістична відповідь.
  const sparsenessHint =
    titleWordCount <= 3 && descLen < 40
      ? "Постановник дав ДУЖЕ короткий запит. Поверни лише goal (повтори заголовок як мету) + короткий чекліст. Не вигадуй scope/deliverables/criteria/дедлайн/години."
      : "";
  return [
    `Постановник: ${input.assignerName || "—"}`,
    `Сьогодні: ${input.today} (${weekday})`,
    `Проєкт: ${input.projectTitle || "—"}, поточна стадія: ${input.projectStage || "—"}`,
    "",
    "Короткий заголовок від постановника:",
    input.title,
    "",
    "Короткий опис від постановника (може бути порожнім):",
    input.description?.trim() || "—",
    "",
    sparsenessHint,
    "Сформуй ТЗ згідно з інструкціями у system prompt. Памʼятай золоте правило: не вигадуй те, чого постановник не сказав.",
  ]
    .filter(Boolean)
    .join("\n");
}
